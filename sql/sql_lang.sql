/*
    SQL делится на следующие подразделения подязыка
        data definitial language DDL - язык определения структуры данных
            начинается на CREATE, ALTER, DROP, TRUNCATE
        data manipulation language DML - язык манипуляции данных
            INSERT, UPDATE, DELETE 
        data control language DCL - язык контроля данными
            GRANT, REVOKE
        transation control language TCL - язык контроля транзакций
            COMMIT, TRANSACTION, ROLLBACK
        data query language DQL - язык запроса данных
            SELECT
*/